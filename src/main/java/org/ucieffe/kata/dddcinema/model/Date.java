package org.ucieffe.kata.dddcinema.model;

public class Date {
    public final int year;
    public final int month;
    public final int dayOfTheMonth;

    public Date(int year, int month, int dayOfTheMonth) {
        this.year = year;
        this.month = month;
        this.dayOfTheMonth = dayOfTheMonth;
    }
}
